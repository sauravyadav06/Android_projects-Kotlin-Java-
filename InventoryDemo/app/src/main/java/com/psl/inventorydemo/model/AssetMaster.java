public class AssetMaster {
    String AssetID,AssetName,AssetType,AssetTypeID,AssetTagID,AssetSerialNo;

    public String getAssetID() {
        return AssetID;
    }

    public void setAssetID(String AssetID) {
        this.AssetID = AssetID;
    }

    public String getAssetName() {
        return AssetName;
    }

    public void setAssetName(String AssetName) {
        this.AssetName = AssetName;
    }

    public String getAssetType() {
        return AssetType;
    }

    public void setAssetType(String AssetType) {
        this.AssetType = AssetType;
    }

    public String getAssetTypeID() {
        return AssetTypeID;
    }

    public void setAssetTypeID(String AssetTypeID) {
        this.AssetTypeID = AssetTypeID;
    }

    public String getAssetTagID() {
        return AssetTagID;
    }

    public void setAssetTagID(String AssetTagID) {
        this.AssetTagID = AssetTagID;
    }

    public String getAssetSerialNo() {
        return AssetSerialNo;
    }

    public void setAssetSerialNo(String AssetSerialNo) {
        this.AssetSerialNo = AssetSerialNo;
    }
}
